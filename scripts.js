// Print out all odd numbers from 1 to 20 //
for (var i = 1; i < 21; i += 2) {
    console.log(i);
}


// Sum numbers from 1 to 5, printing out the current number and sum so far at each step of the way //
var sum = 0;
for (var i = 0; i < 6; i++) {
    sum = sum + i;
    console.log("Num: ", i, ", Sum: ", sum);
}